#include "jedna.h"

int soucet(int x, int y)
        {
        return (x+y);
        }

int odecitani(int x, int y)
        {
        return (x-y);
        }

int nasobeni(int x, int y)
        {
        return (x*y);
        }

int deleni(int x, int y)
        {
        return (x/y);
        }

int tretimocnina(int x)
        {
        return (x*x*x);
        }

int celociselnedeleni (int x, int y)
        {
        return (x%y);
        }
